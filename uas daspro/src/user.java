public class user {
    private String nama;
    private String email;
    private int iduser;

    public void setIduser(int iduser) {
        this.iduser = iduser;
    }

    public int getIduser() {
        return this.iduser;
    }

    public void setUser(String nama) {
        this.nama = nama;
    }

    public String getUsername() {
        return this.nama;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getEmail() {
        return this.email;
    }

}